package de.cosmicsand.webtools.path;

public class PathAtomIndexOutOfBoundsException extends RuntimeException {

    private static final long serialVersionUID = -310449420687659282L;

    public PathAtomIndexOutOfBoundsException() {
        super();
    }

    public PathAtomIndexOutOfBoundsException(String message, Throwable cause) {
        super(message, cause);
    }

    public PathAtomIndexOutOfBoundsException(String message) {
        super(message);
    }

    public PathAtomIndexOutOfBoundsException(Throwable cause) {
        super(cause);
    }

}
